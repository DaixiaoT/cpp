#include"public.h"

/********************************************************************
 * 函 数 名 : delay_ms
 * 输 入: ten_us
 * 函数功能: ms 延时函数，ms=1 时，大约延时 1ms
 * 输 出: 无
 *********************************************************************/
void delay_ms(u16 ms)
{
	u16 i, j;
	for (i = ms; i > 0; i--)
	{
		for(j=110; j>0; j--);
	}
}

/*******************************************************************************
 * 函 数 名		: delay_10us
 * 函数功能		: 延时函数，ten_us=1 时，大约延时 10us
 * 输 入			: ten_us
 * 输 出			: 无
 *******************************************************************************/
void delay_10us(u16 ten_us)
{
	while (ten_us--)
		;
}
